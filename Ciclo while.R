#Funciona de tal manera que si la condición en () se cumple, 
#entonces se ejecuta lo que está en {}. Esto lo repite de manera
#cíclica hasta que () es FALSE

#ciclo infinito
while(TRUE){
  print("hola")}

while(FALSE){
  print("hola")
}

contador <- 1
while(contador< 12){
  print(contador)
  contador <- contador +1}

número <- (1 - 2) * 5
while(número < 100){
  print(número)
  número <- número + 10
}
